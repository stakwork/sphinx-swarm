<script lang="ts">
  import {
    Button,
    TextInput,
    InlineNotification,
  } from "carbon-components-svelte";
  import Add from "carbon-icons-svelte/lib/Add.svelte";
  import ArrowLeft from "carbon-icons-svelte/lib/ArrowLeft.svelte";
  import { add_peer, list_peers, type LndPeer } from "../api/lnd";
  import * as CLN from "../api/cln";
  import {
    peers as peersStore,
    finishedOnboarding,
    createdPeerForOnboarding,
    isOnboarding,
  } from "../store";
  import { parseClnListPeerRes } from "../helpers/cln";

  $: connection_string = "";
  $: $finishedOnboarding, addDefaultPeer();

  export let back = () => {};
  export let tag = "";
  export let newChannel = (p: LndPeer) => {};
  export let type = "";

  $: peers = $peersStore && $peersStore[tag];

  let show_notification = false;
  let error_notification = false;

  async function addPeer() {
    const connection_array = connection_string && connection_string.split("@");
    if (connection_array.length !== 2) {
      error_notification = true;
      return;
    }
    const pubkey = connection_array[0];
    const host = connection_array[1];

    if (type === "Cln") {
      const peer = await CLN.add_peer(tag, pubkey, host);
      if (peer) {
        connection_string = "";
        show_notification = true;
        const peersData = await CLN.list_peers(tag);
        const parsedRes = await parseClnListPeerRes(peersData);
        peersStore.update((peer) => {
          return { ...peer, [tag]: parsedRes.peers };
        });
        createdPeerForOnboarding.update(() => true);
      } else {
        error_notification = true;
      }
    } else {
      if (await add_peer(tag, pubkey, host)) {
        connection_string = "";
        show_notification = true;
        setTimeout(async () => {
          const peersData = await list_peers(tag);
          peersStore.update((ps) => {
            return { ...ps, [tag]: peersData.peers };
          });
          createdPeerForOnboarding.update(() => true);
        }, 1000);
      } else {
        error_notification = true;
      }
    }
  }

  function addDefaultPeer() {
    if (
      $isOnboarding &&
      $finishedOnboarding.hasBalance &&
      !$finishedOnboarding.hasPeers
    ) {
      connection_string =
        "023d70f2f76d283c6c4e58109ee3a2816eb9d8feb40b23d62469060a2b2867b77f@54.159.193.149:9735";
    }
  }

  $: peersLength = peers && peers.length ? peers.length : "No";
  $: peersLabel = peers && peers.length <= 1 ? "peer" : "peers";
  $: addDisabled = !connection_string;
</script>

<section class="peer-wrap">
  <div class="back" on:click={back} on:keypress={() => {}}>
    <ArrowLeft size={24} />
  </div>

  {#if peers && peers.length}
    <div class="label peers-label">{`${peersLength} ${peersLabel}`}</div>
    <div class="peer-list">
      {#each peers as peer}
        <div class="peer">
          <div class="peer-pubkey">{peer.pub_key}</div>
          <div class="peer-address">{peer.address}</div>
          <Button size="small" kind="tertiary" on:click={() => newChannel(peer)}
            >New Channel</Button
          >
        </div>
      {/each}
    </div>
  {/if}

  <div class="label new-peer-label">New Peer</div>
  {#if show_notification}
    <InlineNotification
      lowContrast
      kind="success"
      title="Success:"
      subtitle="Pair has been added."
      timeout={3000}
      on:close={(e) => {
        e.preventDefault();
        show_notification = false;
      }}
    />
  {/if}
  {#if error_notification}
    <InlineNotification
      lowContrast
      kind="error"
      title="Error:"
      subtitle="Error connecting to peer."
      timeout={3000}
      on:close={(e) => {
        e.preventDefault();
        error_notification = false;
      }}
    />
  {/if}
  <section class="new-peer-form">
    <div class="spacer" />
    <TextInput
      labelText={"Connection String"}
      placeholder={"pubkey@host"}
      bind:value={connection_string}
    />
    <div class="spacer" />
    <center
      ><Button
        disabled={addDisabled}
        class="peer-btn"
        on:click={addPeer}
        size="field"
        icon={Add}>Add Peer</Button
      ></center
    >
  </section>
</section>

